#include <unistd.h>

int	main(int argc, char **argv)
{
	int	c;
	int	d;

	d = argc -1;
	while (d > 0)
	{
		c = 0;
		while (argv[d][c] != '\0')
		{
			write(1, &argv[d][c], 1);
			c++;
		}
		write(1, "\n", 1);
		d--;
	}
	return (0);
}
